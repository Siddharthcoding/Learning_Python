# Arithmetic operators : +, -, *, /, % (modulus), ** (exponentiation)

a = 5+2
b = 6/3

print(a)
print(b)

# Assignment operators : =, +=, -=, *=, /=, %=, **=

c = 10
c += 5
d = c
d **= 2

print(c)
print(d)

# Comparison operators : ==, !=, >, <, >=, <=  They return a boolean value (True or False)

e = 5 != 7
f = 5 == 5

print(e)
print(f)

# Logical operators : and, or, not

g = True or False
h = not True
i = True and False
j = 10 and 5

print(g)
print(h)
print(i)
print(j)